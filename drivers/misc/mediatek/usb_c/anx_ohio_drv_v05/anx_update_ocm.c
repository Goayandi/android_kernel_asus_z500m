#define ANX7418_ADDR 0x50
static int use_block_write = 1;

static void sp_write_reg(u8 device_addr, u8 reg_addr, u8 val)
{
	OhioWriteReg(reg_addr, val);
}

static void sp_read_reg(u8 device_addr, u8 reg_addr, u8 *val)
{
	*val = OhioReadReg(reg_addr);
}

static void sp_write_block_reg(u8 device_addr, u8 reg_addr, u8 size, u8 *buf)
{
	int i;

	if (use_block_write)
		OhioWriteBlockReg(reg_addr, size, buf);
	else {
		for (i = 0; i < size; i++)
			OhioWriteReg(reg_addr + i, buf[i]);
	}
}

static void ProgInit(void)
{
	unchar password[3];

	/* ocm reset */
	/* WriteReg(0x05,  0x10); // must reset ocm before starting programming eeprom */
	sp_write_reg(ANX7418_ADDR, 0x05, 0x10);	/* must reset ocm before starting programming eeprom */

	/* send password */
	password[0] = 0x28;
	password[1] = 0x5C;
	password[2] = 0x4E;
/* WriteBlockReg(0x5D,  3,  password); // send password */
	sp_write_block_reg(ANX7418_ADDR, 0x5D, 3, password);	/* send password */
}


/**************EEPROM read and write function start********************/
/*
 * write EEPROM data by burst mode,  every time write 8Byets,
 * if not equal 8,  make it align with 8bytes,  filled with 0xff
 */
static void eeprom_burst_write(unsigned int eeprom_addr, const unsigned char *buf)
{
/* unchar c; */



	/* eeprom address LSBs [7:0] */
/* WriteReg(0x62,  (eeprom_addr & 0xff)); */
	sp_write_reg(ANX7418_ADDR, 0x62, (eeprom_addr & 0xff));

	/*eeprom address MSBs [15:8] */
/* WriteReg(0x63,  (eeprom_addr >> 8 ) & 0xff); */
	sp_write_reg(ANX7418_ADDR, 0x63, (eeprom_addr >> 8) & 0xff);

	/* burn 8bytes into eeprom every time */
/* WriteBlockReg(0x67,  8,  buf); */
	sp_write_block_reg(ANX7418_ADDR, 0x67, 8, (unsigned char *)buf);


/* WriteReg(0x61,  0x11); // enable burst write mode */
	sp_write_reg(ANX7418_ADDR, 0x61, 0x11);	/* enable burst write mode */

/* WriteReg(0x60,  0xC9); // start write */
	sp_write_reg(ANX7418_ADDR, 0x60, 0xC9);	/* start write */

/* WriteReg(0x61,  0x01); // disable burst write mode */
	sp_write_reg(ANX7418_ADDR, 0x61, 0x01);	/* disable burst write mode */
	/* TRACE("."); */


	/* wait for write done */
	/* msleep(1); */
	{
		u8 c;

		do {
			msleep(20);
			sp_read_reg(ANX7418_ADDR, 0x66, &c);
		} while (BITTESTFALSE(c, 0));
	}


}



/* write EEPROM data interface ,  if length is not 8btes align,  filled 0xff as reserved */
static void eeprom_flash(unsigned int eeprom_begin_addr, unsigned int len, const unsigned char *buf)
{
	unsigned int i = 0;	/* length index */
	unsigned int j;
	unsigned char fill_buf[8] = { 0x0 };
	unsigned int total_len = len;
	bool need_fill_align = 0;

	ProgInit();
	/* init EEPROM address */

	pr_warn("%s %s : ProgInit finished!\n", LOG_TAG, __func__);


	if (len % 8 != 0) {
		total_len = (len - len % 8 + 8);
		need_fill_align = 1;
	}

	if (need_fill_align) {
		for (i = 0; i < (len - len % 8); i += 8) {
			eeprom_burst_write(eeprom_begin_addr + i, buf + i);
			pr_warn("%s %s : Fill align eeprom_burst_write 0x%x\n", LOG_TAG, __func__,
				i);
		}

		/* fill the last bytes with 0xff data */
		j = 0;
		for (i = (len - len % 8); i < len; i++)
			fill_buf[j++] = buf[i];
		for (i = len; i < total_len; i++)
			fill_buf[j++] = 0xff;
		eeprom_burst_write(eeprom_begin_addr + (len - len % 8), fill_buf);
	} else {
		for (i = 0; i < len; i += 8) {
			pr_warn("%s %s :  eeprom_burst_write 0x%x\n", LOG_TAG, __func__, i);
			eeprom_burst_write(eeprom_begin_addr + i, buf + i);
		}
	}

}


void anx7418_update(int version, int block_write)
{

	pr_warn("%s %s : ANX7418 update 0x%x begin...\n", LOG_TAG, __func__, version);

	use_block_write = block_write;

	if (version == 0x7)
		eeprom_flash(0, ANX7418_OCM_SIZE, ANX7418_OCM);
	else if (version == 0x10)
		eeprom_flash(0, ocm_crc_bin_len, ocm_crc_bin);
	else if (version == 0x16)
		eeprom_flash(0, ocm_crc_bin_1v6_len, ocm_crc_bin_1v6);
	else if (version == 0xaa)
		eeprom_flash(0, ocm_crc_bin_0xaa_len, ocm_crc_bin_0xaa);

	pr_warn("%s %s : ANX7418 update end!\n", LOG_TAG, __func__);

}


unsigned char eeprom_read_byte(unsigned int eeprom_begin_addr)
{
	unchar c;

/* WriteReg(0x63,  (eeprom_begin_addr >> 8 ) & 0xff); // update [15:8] */
	sp_write_reg(ANX7418_ADDR, 0x63, (eeprom_begin_addr >> 8) & 0xff);	/* update [15:8] */


	/* WriteReg(0x62,  (eeprom_begin_addr & 0xff)); // update [7:0] */
	sp_write_reg(ANX7418_ADDR, 0x62, (eeprom_begin_addr & 0xff));	/* update [7:0] */

	/* WriteReg(0x60,  0xCA); // start read */
	sp_write_reg(ANX7418_ADDR, 0x60, 0xCA);	/* start read */


	/* wait for read done */

/* while(BITTESTFALSE(ReadReg(0x66),  0))            ; */
	do {
		msleep(20);
		sp_read_reg(ANX7418_ADDR, 0x66, &c);
	} while (BITTESTFALSE(c, 0));

	sp_read_reg(ANX7418_ADDR, 0x65, &c);


	return c;
}

